%Internal rule that actually genertes shortest path
paths(_,_,[],L1,L2) :- L1 = [], L2 = [].
paths(X,Y,[H|T],L1,L2) :-
                        path_int(X,H,[H],P1), flatten(P1, Flat1), path_split(X,Flat1,Path1s),
