%These are internal rules used to determine relationships
%Can be queried, keep in mind they are of the form X is the <rule> of Y
father_of(X,Y) :- parent_of(X,Y), male(X).
mother_of(X,Y) :- parent_of(X,Y), female(X).

half_sister_of_f(X,Y) :- female(X), father_of(F,Y), father_of(F,X), X \== Y.
half_sister_of_m(X,Y) :- female(X), mother_of(F,Y), mother_of(F,X), X \== Y.
half_sister_of(X,Y) :- (half_sister_of_f(X,Y) ; half_sister_of_m(X,Y)),
              not(sister_of(X,Y)).

sister_of(X,Y) :- half_sister_of_f(X,Y), half_sister_of_m(X,Y).

half_brother_of_f(X,Y) :- male(X), father_of(F,Y), father_of(F,X), X \== Y.
half_brother_of_m(X,Y) :- male(X), mother_of(F,Y), mother_of(F,X), X \== Y.
half_brother_of(X,Y) :- (half_brother_of_f(X,Y) ; half_brother_of_m(X,Y)),
              not(brother_of(X,Y)).

brother_of(X,Y) :- half_brother_of_f(X,Y), half_brother_of_m(X,Y).

half_sibling_of(X,Y) :- (half_brother_of(X,Y) ; half_sister_of(X,Y)),
              not(sibling_of(X,Y)).
sibling_of(X,Y) :- (brother_of(X,Y) ; sister_of(X,Y)).

ancestor_of(X,Y) :- parent_of(X,Y).
ancestor_of(X,Y) :- parent_of(X,Z), ancestor_of(Z,Y).


%These rules are used for relationships, returns list satisfying rule
%Of the form Y are all of the <rule> of X
half_sibs(X,Y) :- findall(Z, half_sibling_of(X,Z),R), sort(R,Y); true.
half_bros(X,Y) :- findall(Z, (half_sibling_of(X,Z),male(Z)),R), sort(R,Y); true.
half_sis(X,Y) :- findall(Z, (half_sibling_of(X,Z),female(Z)),R), sort(R,Y); true.

sibs(X,Y) :- findall(Z, sibling_of(X,Z),R), sort(R,Y); true.
bros(X,Y) :- findall(Z, brother_of(Z,X),R), sort(R,Y); true.
sis(X,Y) :- findall(Z, sister_of(Z,X), R), sort(R,Y); true.
kids(X,Y) :- findall(Z, parent_of(X,Z), R), sort(R,Y); true.
pars(X,Y) :- findall(Z, parent_of(Z,X), Y).
kids_with(X,Y) :- findall(Z, (parent_of(X,A), parent_of(Z,A), Z \== X), R), sort(R,Y) ; true.

decs(X,Y) :- findall(Z, ancestor_of(X,Z), R), sort(R,Y); true.
ancs(X,Y) :- findall(Z, ancestor_of(Z,X), R), sort(R,Y) ; true.


